	private void setResumeDriverActivityChart(Driver driver, Date registerDate) throws Exception {
		Map<TYPE, Float> cardActivityDailyChangeGraphs = null;
		try {
			cardActivityDailyChangeGraphs = cardActivityDailyService.getCardActivityDailyResumeByDriver(driver, registerDate);
		}
		catch (Exception ex) {
			ex.getMessage();
		}
				
		driverActivityChart = new Chart(ChartType.PIE);
		driverActivityChart.setSizeFull();
				
		Configuration conf = driverActivityChart.getConfiguration();

        conf.setTitle("Resume Daily Activity Driver");
        conf.setSubTitle("Source: " + registerDate.toString());
        
        PlotOptionsPie plotOptions = new PlotOptionsPie();
        plotOptions.setAllowPointSelect(true);
        plotOptions.setCursor(Cursor.POINTER);
        Labels dataLabels = new Labels();
        dataLabels.setEnabled(true);
        dataLabels.setFormatter("''+ this.point.name +': '+ Math.round(this.percentage) +' %'");
        plotOptions.setDataLabels(dataLabels);
        conf.setPlotOptions(plotOptions);
        
        final DataSeries series = new DataSeries();
        for (Map.Entry<TYPE, Float> entry : cardActivityDailyChangeGraphs.entrySet()) {        	
        	TYPE type = entry.getKey();        	
        	Float value = entry.getValue();        	
        	
        	SolidColor color = null;
        	if (type.equals(CardActivityChange.TYPE.UNKNOWN))
        		color = new SolidColor(CardActivityChange.UNKNOWN_HEX_COLOR); // PURPLE;
            else if (type.equals(CardActivityChange.TYPE.BREAK_REST))
            	color = new SolidColor(CardActivityChange.BREAK_REST_HEX_COLOR); // RED	            	 
            else if (type.equals(CardActivityChange.TYPE.SHORT_BREAK))
            	color = new SolidColor(CardActivityChange.SHORT_BREAK_HEX_COLOR); // ORANGE
            else if (type.equals(CardActivityChange.TYPE.AVAILABLE))
            	color = new SolidColor(CardActivityChange.AVAILABLE_HEX_COLOR); // BLACK
            else if (type.equals(CardActivityChange.TYPE.WORKING))
            	color = new SolidColor(CardActivityChange.WORKING_HEX_COLOR); // YELLOW					 
            else if (type.equals(CardActivityChange.TYPE.DRIVING))
            	color = new SolidColor(CardActivityChange.DRIVING_HEX_COLOR); // GREEN
        	        	
        	series.add(new DataSeriesItem(type.name(), value, color));
        }
        conf.setSeries(series);
        
        driverActivityChart.addPointClickListener(new PointClickListener() {
            @Override
            public void onClick(PointClickEvent event) {
            	double y = series.get(event.getPointIndex()).getY().doubleValue();
            	double Hours = Math.floor(y);     
            	double Minutes = (y - Hours) * 60;  
            	String val = Hours + ":" + Minutes;
            	
                Notification.show("Time: "  + val);
            }
        });
                
        driverActivityChart.drawChart(conf);
        
        resumeDriverActivityChartLayout.addComponent(driverActivityChart);
	}
	
